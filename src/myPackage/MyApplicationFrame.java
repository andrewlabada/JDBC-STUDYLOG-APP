    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        try {
            // TODO add your handling code here:
            Logs log = new Logs();
            
            int i =jTable2.getSelectedRow();
            TableModel model = jTable2.getModel();
            String dt = model.getValueAt(i, 1).toString();
            
            
            Date sdate = new SimpleDateFormat("dd-MM-yyyy").parse(dt);
            jDate.setDate(sdate);
            titleTF.setText(model.getValueAt(i,2).toString());
            descriptionTF.setText(model.getValueAt(i,3).toString());
            
        } catch (ParseException ex) {
            Logger.getLogger(MyApplicationFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jTable2MouseClicked
