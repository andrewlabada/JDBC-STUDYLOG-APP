    private void saveBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBTNActionPerformed
        // TODO add your handling code here:
        Logs log = new Logs();
        LogInformation loginfo = new LogInformation();
        SimpleDateFormat sdate = new SimpleDateFormat("dd-MM-yyyy");
    
        String date = sdate.format(jDate.getDate());
        String title = titleTF.getText();
        String description = descriptionTF.getText();
        
        
        if(title.trim().equals("") || description.trim().equals("")){
        }
        else{
            log.setDate(date);
            log.setTitle(title);
            log.setDescription(description);
            loginfo.addInformation(log);
            DefaultTableModel model = (DefaultTableModel)jTable2.getModel();
            model.setRowCount(0);
            displayDB();
        }
        
        
        
    }//GEN-LAST:event_saveBTNActionPerformed

    private void updateBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBTNActionPerformed
        // TODO add your handling code here:
        Logs log = new Logs();
        LogInformation loginfo = new LogInformation();
        SimpleDateFormat sdate = new SimpleDateFormat("dd-MM-yyyy");
         
        int row = jTable2.getSelectedRow();
        String value = (jTable2.getModel().getValueAt(row,0).toString());
        
        String date = sdate.format(jDate.getDate());
        String title = titleTF.getText();
        String description = descriptionTF.getText();
        
        log.setID(parseInt(value));
        log.setDate(date);
        log.setTitle(title);
        log.setDescription(description);
        loginfo.updateInformation(log);
        
        DefaultTableModel model = (DefaultTableModel)jTable2.getModel();
        model.setRowCount(0);
        displayDB();
        
    }//GEN-LAST:event_updateBTNActionPerformed
